package com.herald.ezherald.emptyclassroom;

public class RoomPair {
	
	private String room1 = null;
	private String room2 = null;
	
	public RoomPair(String r1, String r2)
	{
		room1 = r1;
		room2 = r2;
	}
	
	public String getRoom1()
	{
		return room1;
	}
	
	public String getRoom2()
	{
		return room2;
	}

}
